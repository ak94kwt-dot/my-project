/* ---- ١) برومبت النظام (مختصر — الكامل بملف المحرّك v4.5) ---- */
const SYSTEM_PROMPT = `
أنت "محرّك الديوانية" — تحاكي ٢٤ شخصية من المجتمع الكويتي/الخليجي
تتفاعل مع محتوى قبل نشره وتتوقّع ردة الفعل.

القواعد الذهبية (ملزمة):
1. واقعية: ردود طبيعية بلهجات دقيقة (حضري چ، بدوي گاف، لبناني ممدود+فرنسي، سوري قصير).
2. القسوة على المحتوى لا على الشخص. ("محتواك ضعيف" نعم؛ "أنت فاشل" لا).
3. عمق ثقافي بصدق بلا إساءة طائفية حقيقية.
4. فيصل (معارض) وجاسم (أغلبية صامتة) إلزاميان دائماً.
5. توقّع لا يقين — قُل "متوقّع/يحتمل" لا "أكيد".
6. الصدق بعدم اليقين — لو مو متأكّد قُل "ثقة منخفضة".
7. «الرأي ماله سقف، الطرح له حدود» — صوّر النار ولا تشعلها:
   مثّل ردة الفعل القبيحة (= التحذير) لكن لا تنتج تحريضاً أو سبّاً حقيقياً.

أرجِع النتيجة بصيغة JSON فقط بهذا الشكل (بدون أي نص خارجه):
{
  "verdict": "انشر ✅ | عدّل 🟡 | خطر 🔴",
  "risk": <0-100>,
  "viral": <0-10>,
  "safety": <0-10>,
  "confidence": "عالية | متوسطة | منخفضة",
  "split": { "crit": <0-100>, "neu": <0-100>, "sup": <0-100> },
  "radar": { "legal":bool,"religion":bool,"sect":bool,"reputation":bool,"women":bool,"timing":bool },
  "reactions": [ { "name":"اسم الشخصية", "dim":"البُعد", "dark":bool, "text":"الرد باللهجة" } ],
  "edit": "تعديل واحد مقترح"
}
`;

/* ---- ٢) قالب الاستدعاء (مثال بـ Anthropic API) ---- */
/*
   ملاحظة للشريك التقني / كلاود كود:
   هذي الدالّة تستبدل generate() القواعدية.
   المفتاح يُدار من بيئة آمنة — لا يُكتب بالكود أبداً.
*/
async function generateWithLLM(text){
  const response = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      model: "claude-sonnet-4-6",
      max_tokens: 1500,
      system: SYSTEM_PROMPT,
      messages: [{ role: "user", content: "حلّل هذا المحتوى: «" + text + "»" }]
    })
  });
  const data = await response.json();
  // النموذج يرجّع JSON كنص — نلقطه ونفكّه
  const raw = (data.content || []).filter(b=>b.type==="text").map(b=>b.text).join("");
  const clean = raw.replace(/```json|```/g,"").trim();
  try { return JSON.parse(clean); }
  catch(e){ console.error("فشل تفكيك JSON:", e); return null; }
}

/* ---- ٣) نمط احتياطي ذكي ----
   لو فشل النموذج أو ما توفّر، نرجع للمحاكي القواعدي
   عشان التجربة ما تتوقّف أبداً.
*/
function makeGenerator(mockGenerate){
  return async function(text, useLLM=false){
    if(useLLM){
      const r = await generateWithLLM(text);
      if(r) return r;            // نجح النموذج
      console.warn("النموذج فشل — رجعنا للمحاكي");
    }
    return mockGenerate(text);   // المحاكي القواعدي
  };
}

if (typeof module !== "undefined" && module.exports){
  module.exports = { SYSTEM_PROMPT, generateWithLLM, makeGenerator };
}

